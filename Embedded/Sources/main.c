static void timers_init(void) {
    uint32_t err_code;
    uint32_t time_ms = 100;
    uint32_t time_ticks;

    // New timers
    nrf_drv_timer_config_t timer_cfg = NRF_DRV_TIMER_DEFAULT_CONFIG;
    err_code = nrf_drv_timer_init(&TIMER_DISPATCH, &timer_cfg, &hnd_dispatchNetMsg2);
    APP_ERROR_CHECK(err_code);

    time_ticks = nrf_drv_timer_ms_to_ticks(&TIMER_DISPATCH, time_ms);
    nrf_drv_timer_extended_compare(
	     &TIMER_DISPATCH, NRF_TIMER_CC_CHANNEL0, time_ticks, NRF_TIMER_SHORT_COMPARE0_CLEAR_MASK, true);

}



