/**@brief Function for the GAP initialization.
 *
 * @details This function will set up all the necessary GAP (Generic Access Profile) parameters of
 *          the device. It also sets the permissions and appearance.
 */
static void gap_params_init(void)
{
    uint32_t                err_code;
    ble_gap_conn_params_t   gap_conn_params;
    ble_gap_conn_sec_mode_t sec_mode;

    BLE_GAP_CONN_SEC_MODE_SET_OPEN(&sec_mode);

    err_code = sd_ble_gap_device_name_set(&sec_mode,
                                          (const uint8_t *) defaultDeviceName,
                                          strlen(defaultDeviceName));
    APP_ERROR_CHECK(err_code);

    memset(&gap_conn_params, 0, sizeof(gap_conn_params));

    gap_conn_params.min_conn_interval = MIN_CONN_INTERVAL;
    gap_conn_params.max_conn_interval = MAX_CONN_INTERVAL;
    gap_conn_params.slave_latency     = SLAVE_LATENCY;
    gap_conn_params.conn_sup_timeout  = CONN_SUP_TIMEOUT;

    err_code = sd_ble_gap_ppcp_set(&gap_conn_params);
    APP_ERROR_CHECK(err_code);
}


/**@brief Function for handling the data from the Nordic UART Service.
 *
 * @param[in] p_nus    Nordic UART Service structure.
 * @param[in] p_data   Data to be send to UART module.
 * @param[in] length   Length of the data.
 */
static void nus_data_handler(ble_nus_evt_t * p_evt)
{
    if (p_evt->type == BLE_NUS_EVT_RX_DATA)
    {
        //uint32_t err_code;

        NRF_LOG_DEBUG("Received data from BLE NUS.");
        NRF_LOG_HEXDUMP_DEBUG(p_evt->params.rx_data.p_data, p_evt->params.rx_data.length);

        uint16_t len = p_evt->params.rx_data.length > (m_len_buf-1) ? (m_len_buf-1) : p_evt->params.rx_data.length;

        memset(m_rx_buf, 0, m_len_buf);
        memcpy(m_rx_buf, p_evt->params.rx_data.p_data, len);
        m_rx_buf[len] = '\n';
        len++;

    	if (memcmp(m_rx_buf, "get ver", 7) == 0){
    		len = sizeof(_versionDefault);
    		ble_nus_string_send(&m_nus, (uint8_t *)_versionDefault, &len );
    		return;
    	}else if (memcmp(m_rx_buf, "run dfu", 7) == 0){
    	    nrf_power_gpregret_set(BOOTLOADER_DFU_START);
    	    NVIC_SystemReset();
    		return;
    	}else {
    		ble_nus_string_send(&m_nus, m_rx_buf, &len);
    	}
    }
}


